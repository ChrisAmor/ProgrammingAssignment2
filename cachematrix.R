## These functions build a cacheable matrix and a function to invert the matrix either from the cache or by inverting and then cacheing the matrix

## Accepts a matrix and makes the inverse cacheable with associated get and and set functions

makeCacheMatrix <- function(x = matrix()) {

        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setinverse <- function(inverse) m <<- inverse
        getinverse <- function() m
        list(set = set, get = get,
             setinverse = setinverse,
             getinverse = getinverse)
}

## Accepts a makecacheMatrix object and returns the cached inverse or calculates and caches the inverse if not already cached

